#include<iostream>
using namespace std;
int main(){
	cout<<"size of int:"<<sizeof(int)<<endl;
	cout<<"size of char:"<<sizeof(char)<<endl;
	cout<<"size of float:"<<sizeof(float)<<endl;
	cout<<"size of double:"<<sizeof(double)<<endl;
	cout<<"size of 34.45:"<<sizeof(34.45f)<<endl;
	cout<<"size of 12.45f:"<<sizeof(12.45)<<endl;
	cout<<"size of 12.45L:"<<sizeof(12.45L)<<endl;
	
	return 0;
}
